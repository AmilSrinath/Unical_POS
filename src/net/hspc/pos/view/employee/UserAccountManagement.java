    private void loadAllUsers() {
        try {
            ArrayList<MainUserDto> allUsers=userAcountManagementController.getAllUsers();
        
            DefaultTableModel dtm=(DefaultTableModel) userDetailsTbl.getModel();
            dtm.setRowCount(0);
            
            for(MainUserDto dto: allUsers){
                Object[] rowData={
                    dto.getUserId(),
                    dto.getEmployee(),
                    dto.getUserRole(),
                    dto.getUserName(),
                    dto.getPassword(),
                    dto.isStatus()
                };
                dtm.addRow(rowData);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(UserAccountManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
