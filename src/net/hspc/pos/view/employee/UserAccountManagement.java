    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
                MainUserDto mainUserDto=new MainUserDto(0, employeeCmb.getSelectedItem().toString()
                , userRoleCmb.getSelectedItem().toString(), userNameTxt.getText(), 
                        passwordTxt.getText(),isActiveBox.isSelected());
        
        if(!update){
            try {
                 boolean result=userAcountManagementController.saveUser(mainUserDto);
                 if (result) {
                    configTimer.setMessageTimer(true);
                 } else {
//                     JOptionPane.showMessageDialog(this, "Failed to add the Doctor");
                 }
             } catch (Exception ex) {
                 Logger.getLogger(UserAccountManagement.class.getName()).log(Level.SEVERE, null, ex);
             }
        }else{
            try {
                if(userDetailsTbl.getSelectedRow()==-1){
                    return;
                }
                
                mainUserDto.setUserId((int) userDetailsTbl.getValueAt(userDetailsTbl.getSelectedRow(), 0));
                boolean result=userAcountManagementController.updateUser(mainUserDto);
                if (result) {
                    configTimer.setMessageTimer(true);
                 } else {
                    
                 }

            } catch (Exception ex) {
                Logger.getLogger(UserAccountManagement.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        loadAllUsers();
    }//GEN-LAST:event_saveBtnActionPerformed

