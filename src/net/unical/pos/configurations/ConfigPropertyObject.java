package net.unical.pos.configurations;

/**
 *
 * @author Sanjuka
 */
public class ConfigPropertyObject {
    
    private String serverHost;
    private String serverPort;
    private String serverDb;
    private String serverUser;
    private String serverPw;
    private String serverPwd;
    private String splash_screen;

    public String getServerHost() {
        return serverHost;
    }

    public void setServerHost(String serverHost) {
        this.serverHost = serverHost;
    }

    public String getServerPort() {
        return serverPort;
    }

    public void setServerPort(String serverPort) {
        this.serverPort = serverPort;
    }

    public String getServerDb() {
        return serverDb;
    }

    public void setServerDb(String serverDb) {
        this.serverDb = serverDb;
    }

    public String getServerUser() {
        return serverUser;
    }

    public void setServerUser(String serverUser) {
        this.serverUser = serverUser;
    }

    public String getServerPw() {
        return serverPw;
    }

    public void setServerPw(String serverPw) {
        this.serverPw = serverPw;
    }

    public String getSplash_screen() {
        return splash_screen;
    }

    public void setSplash_screen(String splash_screen) {
        this.splash_screen = splash_screen;
    }

    public String getServerPwd() {
        return serverPwd;
    }

    public void setServerPwd(String serverPwd) {
        this.serverPwd = serverPwd;
    }
    
    
}
