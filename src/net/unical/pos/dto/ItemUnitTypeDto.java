package net.unical.pos.dto;

/**
 *
 * @author Sanjuka
 */
public class ItemUnitTypeDto {
    
    Integer unitTypeId;
    String name;
    Integer status;
    Integer userId;
    Integer visible;

    public ItemUnitTypeDto() {
    }

    public ItemUnitTypeDto(Integer unitTypeId, String name, Integer status, Integer userId, Integer visible) {
        this.unitTypeId = unitTypeId;
        this.name = name;
        this.status = status;
        this.userId = userId;
        this.visible = visible;
    }

    public Integer getUnitTypeId() {
        return unitTypeId;
    }

    public void setUnitTypeId(Integer unitTypeId) {
        this.unitTypeId = unitTypeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getVisible() {
        return visible;
    }

    public void setVisible(Integer visible) {
        this.visible = visible;
    }
    
    
}
