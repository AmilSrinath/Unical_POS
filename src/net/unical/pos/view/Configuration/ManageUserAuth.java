    private void resonTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_resonTableMousePressed
        if (evt.getClickCount() == 2) {
            int selectedRow = resonTable.getSelectedRow();
            if (selectedRow != -1) {
                selectedUserId = Integer.parseInt(resonTable.getValueAt(selectedRow, 0).toString()); // Assuming reson_id is the 1st column
                String resonText = resonTable.getValueAt(selectedRow, 1).toString();
                
                ModelOptions.setLocationRelativeTo(null);
                ModelOptions.setSize(530, 450);
                ModelOptions.setLocationRelativeTo(null);
                ModelOptions.setTitle("Actions");
                ModelOptions.setVisible(true);
                
                ArrayList<AuthModuleWise> authModuleWises = manageUserAuthRepositoryImpl.getAllModuleWiseByUserID(selectedUserId);
                
                btnDashboard.setSelected(false);
                btnSales.setSelected(false);
                btnInventory.setSelected(false);
                btnDeliveryOrders.setSelected(false);
                btnFilterOrder.setSelected(false);
                btnPayment.setSelected(false);
                btnInquiry.setSelected(false);
                btnPMS.setSelected(false);
                btnEmployee.setSelected(false);
                btnPropertyManagement.setSelected(false);
                btnConfiguration.setSelected(false);

                // Set selected based on module_id
                for (AuthModuleWise auth : authModuleWises) {
                    switch (auth.getModule_id()) {
                        case 1: btnDashboard.setSelected(auth.getStatus() == 1); break;
                        case 2: btnSales.setSelected(auth.getStatus() == 1); break;
                        case 3: btnInventory.setSelected(auth.getStatus() == 1); break;
                        case 4: btnDeliveryOrders.setSelected(auth.getStatus() == 1); break;
                        case 5: btnFilterOrder.setSelected(auth.getStatus() == 1); break;
                        case 6: btnPayment.setSelected(auth.getStatus() == 1); break;
                        case 7: btnInquiry.setSelected(auth.getStatus() == 1); break;
                        case 8: btnPMS.setSelected(auth.getStatus() == 1); break;
                        case 9: btnEmployee.setSelected(auth.getStatus() == 1); break;
                        case 10: btnPropertyManagement.setSelected(auth.getStatus() == 1); break;
                        case 11: btnConfiguration.setSelected(auth.getStatus() == 1); break;
                    }
                }
            }
        }
    }//GEN-LAST:event_resonTableMousePressed

