    private Integer selectedUserId = null;
    
    public void getAllResons() {
        DefaultTableModel dtm = (DefaultTableModel) resonTable.getModel();
        dtm.setRowCount(0);

        List<UserAuth> userAuths = manageUserAuthRepositoryImpl.getAllUsers();
        for (UserAuth model : userAuths) {
            Object[] row = {
                model.getUser_id(),
                model.getEmp_name(),
                model.getUser_role()
            };
            dtm.addRow(row);
        }

        resonTable.setModel(dtm);
    }

    
