    private void loadAllDesignations() {
        try {
            String query="WHERE visible=1";
            ArrayList<EmployeeDesignationDto> designationDtos=designationController.getAllDesignations(query);
            
            DefaultTableModel dtm=(DefaultTableModel) designationTbl.getModel();
            dtm.setRowCount(0);
            
            for(EmployeeDesignationDto dto: designationDtos){
                Object[] rowData={
                    dto.getDesignationId(),dto.getDesignationName(),
                    dto.getStatus()
                };
                dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(EmployeeDesignation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
