    private void loadAllSuppliers() {
        try {
            ArrayList<SupplierDto> allSuppliers=supplierController.getAll();
        
            DefaultTableModel dtm=(DefaultTableModel) supplierListTbl.getModel();
            dtm.setRowCount(0);
            
            for(SupplierDto dto: allSuppliers){
                Object[] rowData={
                    dto.getCompanyName(),
                    dto.getPhone(),
                    dto.getAddress(),
                    dto.getStatus()
                };
                dtm.addRow(rowData);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(SupplierManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
