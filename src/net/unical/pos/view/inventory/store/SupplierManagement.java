    private void jXButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jXButton1ActionPerformed
        
        Integer activeStatus=0;
        
        if(statusBox.isSelected()){
            activeStatus=1;
        }
        
        SupplierDto supplierDto=new SupplierDto(0, 
                salesmanNameTxt.getText(), 
                companyNameTxt.getText(),
                brandNameTxt.getText(),
                Integer.parseInt(telephoneTxt.getText()),
                Integer.parseInt(mobileTxt.getText()),
                addressTxt.getText(),
                emailTxt.getText(),
                activeStatus,
                dashboard.CREATED_USER_ID,
                1);
        
        if(!update){
            try {
                 boolean result=supplierController.saveSupplier(supplierDto);
                 if (result) {
                     JOptionPane.showMessageDialog(this, "Successfull!");
                 } else {
//                   JOptionPane.showMessageDialog(this, "Failed to add the Category");
                 }
             } catch (Exception ex) {
                 Logger.getLogger(SupplierManagement.class.getName()).log(Level.SEVERE, null, ex);
             }
        }else{
            try {
                if(supplierListTbl.getSelectedRow()==-1){
                    return;
                }
                
                supplierDto.setSupplierId((int) supplierListTbl.getValueAt(supplierListTbl.getSelectedRow(), 0));
                boolean result=supplierController.updateSupplier(supplierDto);
                if (result) {
                     JOptionPane.showMessageDialog(this, "Successfull!");
                 } else {
//                     JOptionPane.showMessageDialog(this, "Update Fail");
                 }

            } catch (Exception ex) {
                Logger.getLogger(SupplierManagement.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        loadAllSuppliers();
    }//GEN-LAST:event_jXButton1ActionPerformed

